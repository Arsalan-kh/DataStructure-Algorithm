//Problem no 4 Check num if it's even or odd//
var num = prompt("Enter a number");
if (num % 2 == 0) {
    console.log("Number is Even")
} else {
    console.log("Number is Odd")
}

/*Explanation
To solve this we used % operator Then, whether num is perfectly divisible by
 2 or not is checked using the modulus % operator.
*/